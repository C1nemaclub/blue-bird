describe('Math', () => {
  it('should add two numbers', () => {
    const result = 1 + 2
    expect(result).toBe(3)
  })

  it('should subtract two numbers', () => {
    const result = 1 - 2
    expect(result).toBe(-1)
  })

  it('should multiply two numbers', () => {
    const result = 1 * 2
    expect(result).toBe(2)
  })

  it('should divide two numbers', () => {
    const result = 1 / 2
    expect(result).toBe(0.5)
  })
})
